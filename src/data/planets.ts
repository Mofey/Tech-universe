function buildDetailedLesson(
  id: string,
  name: string,
  category: string,
  description: string,
  concepts: string[],
  formula: string,
  index: number,
): string {
  const focus = technologyLessonFocus[id] ?? `Apply ${name} intentionally in ${category}.`;
  const conceptBullets = concepts.map((concept) => `- ${concept}`).join("<br/>");
  const templates = [
    `<strong>${name} Mission Brief</strong><br/>${description}<br/><br/><strong>Practical Focus</strong><br/>${focus}<br/><br/><strong>Core Concepts</strong><br/>${conceptBullets}<br/><br/><strong>Pattern to Remember</strong><br/>${formula}`,
    `<strong>${name} Workflow Lesson</strong><br/>In ${category}, ${name} is usually used to move from raw inputs to decision-ready outputs.<br/><br/><strong>When to Use</strong><br/>${focus}<br/><br/><strong>Working Set</strong><br/>${conceptBullets}<br/><br/><strong>Reference Expression</strong><br/>${formula}`,
    `<strong>${name} Deep Dive</strong><br/>${description}<br/><br/><strong>Execution Strategy</strong><br/>${focus}<br/><br/><strong>Key Building Blocks</strong><br/>${conceptBullets}<br/><br/><strong>Representative Formula / Syntax</strong><br/>${formula}`,
  ];
  return templates[index % templates.length];
}

function buildTechnologyQuiz(
  name: string,
  category: string,
  concepts: string[],
  formula: string,
  index: number,
) {
  const primaryConcept = concepts[0] ?? "Core concept";
  const secondaryConcept = concepts[1] ?? "Key workflow";

  const templates = [
    {
      question: `For ${name}, which concept is most central to day-to-day ${category.toLowerCase()} work?`,
      options: [
        primaryConcept,
        "Hardware procurement",
        "Office documentation formatting",
        "Manual spreadsheet macros only",
      ],
      correct: 0,
      explanation: `${primaryConcept} is a central idea when applying ${name} in ${category.toLowerCase()} workflows.`,
    },
    {
      question: `Which statement best reflects how ${name} is typically used?`,
      options: [
        `${name} mainly replaces all databases`,
        `${name} usually combines ${primaryConcept} and ${secondaryConcept} to solve practical tasks`,
        `${name} is only for toy examples`,
        `${name} cannot be used with teams`,
      ],
      correct: 1,
      explanation: `Real projects with ${name} combine multiple capabilities, especially ${primaryConcept} and ${secondaryConcept}.`,
    },
    {
      question: `Which expression most closely matches a representative ${name} pattern?`,
      options: [formula, "status = always_successful()", "x = x / 0", "while(true) { idle(); }"],
      correct: 0,
      explanation: `${formula} captures a common pattern associated with ${name}.`,
    },
  ];

  return templates[index % templates.length];
}

function makePlanet(
  id: string,
  name: string,
  index: number,
  color: string,
  category: string,
  description: string,
  concepts: string[],
  formula: string,
): Planet {
  const pos = positions[index];
  const size = 78 + ((index % 4) * 10 + 8);
  const quiz = buildTechnologyQuiz(name, category, concepts, formula, index);

  return {
    id,
    name,
    size,
    x: pos.x,
    y: pos.y,
    color,
    gradient: `linear-gradient(135deg, ${color} 0%, #0b1022 100%)`,
    glowColor: `${color}80`,
    description,
    category,
    difficulty: index % 3 === 0 ? "Beginner" : index % 3 === 1 ? "Intermediate" : "Advanced",
    stats: {
      power: 70 + ((index * 7) % 30),
      versatility: 72 + ((index * 9) % 28),
      learning: 62 + ((index * 11) % 34),
    },
    concepts,
    keyFormula: formula,
    detailedMath: buildDetailedLesson(id, name, category, description, concepts, formula, index),
    quiz,
  };
}

